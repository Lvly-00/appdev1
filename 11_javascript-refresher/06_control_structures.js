// If-Else Statements
const grades = 95;

if (grades >= 90) {
  console.log("A");
} else if (grades >= 80) {
  console.log("B");
} else if (grades >= 70) {
  console.log("C");
} else {
  console.log("F");
}

// For Loop
for (let i = 1; i <= 5; i++) {
  console.log(i);
}

// While Loop
let count = 1;
while (count <= 3) {
  console.log("Hello");
  count++;
}
